#include <SFML/Graphics.hpp>
#include "Tile.h"

// ========================================================================================================================================================//

Tile::Tile(Texture textureIn, float x, float y) : position{x, y}, texture{textureIn} {
    sprite.setPosition(position);
    sprite.setTexture(texture);
};

// ========================================================================================================================================================//

void Tile::draw(sf::RenderTarget &window, sf::RenderStates states) const
{ 
    window.draw(sprite); 
}

// ========================================================================================================================================================//

void Tile::move(std::string const& direction) {
    if (direction[1] == 'D') {
        addY(30);     
    }
    else if (direction[1] == 'L') {
        addX(-30);
    }
    else if (direction[1] == 'R') {
        addX(30);
    }
}

// ========================================================================================================================================================//


void Tile::addX(int x) {
    position.x += x;
    sprite.setPosition(position);
}

// ========================================================================================================================================================//

void Tile::addY(int y) {    
    position.y += y;
    sprite.setPosition(position);
}

// ========================================================================================================================================================//

void Tile::setX(int x) {
    position.x = x;
    sprite.setPosition(position);
}

// ========================================================================================================================================================//

void Tile::setY(int y) {
    position.y = y;
    sprite.setPosition(position);
}